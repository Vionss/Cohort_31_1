public class IntegerNum {
    public static void main(String[] args) {
        // task 1

        int q = 0;
        int w = 1;
        int e = 2;
        int r = 3;
        int t = 4;
        int y = 5;
        int u = 6;
        int i = 7;
        int o = 8;
        int p = 9;

        int a = (q + w + e + r + t + y + u + i + o + p) / 10; // среднее ариф.

        System.out.println(a);

        int h = (q + w + e + r + t + y + u + i + o + p);

        double g = (double) h / (double) 10;

        System.out.println(g);

        double s = 45.0D;
        double d = 10.0D;
        double f = s / d;

        System.out.printf("Result = %.1f ", f);


        // task 2

        int n = 1000;
        int b = 500;
        int c = (n + b) - 100;

        int m = 3;
        int l = 5;
        int k = l - m;

        System.out.println(k);
        System.out.println(c);
    }
}
